public class AssignmentOperator{
    public static void main(String[] args){
        int p=10, q=2;
        p=q;
        System.out.println(p);
        p+=q;
        System.out.println(p);
        p-=q;
        System.out.println(p);
        p*=q;
        System.out.println(p);
        p/=q;
        System.out.println(p);
        p%=q;
        System.out.println(p);
    }

}