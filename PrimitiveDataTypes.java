public class PrimitiveDataTypes{
    public static void main(String[] args) {
        boolean flag = true;
        int a = 1213;
        short b = 2;
        byte c = 100;
        long d = 3432322;
        float e = 431.452f;
        double f = 9312.3221;
        char ch = 'p';
        System.out.println("The Boolean value is " + flag);
        System.out.println("The int value is " + a);
        System.out.println("The short value is " + b);
        System.out.println("The byte value is " + c);
        System.out.println("The long value is " + d);
        System.out.println("The float value is " + e);
        System.out.println("The double value is " + f);
        System.out.println("The char value is " + ch);
    }
}